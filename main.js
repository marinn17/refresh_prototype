if (hour >= 7 && hour < 10)
  document.getElementById("mealTime").innerHTML = "Breakfast";
else if (hour > 11 && hour <= 2) {
  document.getElementById("mealTime").innerHTML = "Lunch";
}
else if (hour >5 && hour <= 7) {
  document.getElementById("mealTime").innerHTML = "Dinner";
}
else {
  document.getElementById("mealTime").innerHTML = "Closed";

}

